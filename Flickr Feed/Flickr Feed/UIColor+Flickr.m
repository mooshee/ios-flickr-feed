#import "UIColor+Flickr.h"

@implementation UIColor (Flickr)

+ (UIColor *)flickrPink {
    return [UIColor colorWithRed:255.0f/255.0f green:0.0f/255.0f blue:132.0f/255.0f alpha:1.0f];
}

+ (UIColor *)flickrBlue {
    return [UIColor colorWithRed:64.0f/255.0f green:150.0f/255.0f blue:238.0f/255.0f alpha:1.0f];
}

@end
